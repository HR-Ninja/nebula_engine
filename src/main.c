	unsigned int indices[] = {  
        0, 1, 3, // first triangle
        1, 2, 3  // second triangle
    };

int main() {

	gl_init();
	window_init("testing", 800, 600);
	glad_init();

	Shader shader = load_shader("assets/vert.glsl", "assets/frag.glsl");
	use_shader(&shader);

	uint32_t VAO, VBO, EBO;
	glGenVertexArrays(1, &VAO);
	glBindVertexArray(VAO);

	glGenBuffers(1, &EBO);
	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, EBO);
    glBufferData(GL_ELEMENT_ARRAY_BUFFER, sizeof(indices), indices, GL_STATIC_DRAW);

	glGenBuffers(1, &VBO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW); 

	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);  

	glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
	glEnableVertexAttribArray(1);  

	Texture t = load_texture("assets/container.jpg");
	bind_texture(&t);

	mat4 model;
	glm_mat4_identity(model);
	vec3 axis = {0.5f, 1.0f, 0.0f};
	
	mat4 view;
	glm_mat4_identity(view);
	vec3 view_position = {0.0f, 0.0f, -3.0f};
	glm_translate(view, view_position);

	mat4 projection;
	glm_mat4_identity(projection);
	glm_perspective(glm_rad(45.0f), 800.0f/600.0f, 0.1f, 100.0f, projection);
	

	uint32_t view_location = glGetUniformLocation(shader.id, "view");
	glUniformMatrix4fv(view_location, 1, GL_FALSE, (const float*)view);

	uint32_t projection_location = glGetUniformLocation(shader.id, "projection");
	glUniformMatrix4fv(projection_location, 1, GL_FALSE, (const float*)projection);

	glEnable(GL_DEPTH_TEST);

	while (window_active()) {
		start_frame();
		
		glm_rotate(model, 2.0f * delta_time, axis);

		uint32_t model_location = glGetUniformLocation(shader.id, "model");
		glUniformMatrix4fv(model_location, 1, GL_FALSE, (const float*)model);

		glDrawArrays(GL_TRIANGLES, 0, 36);

		end_frame();
	}

	return 0;
}